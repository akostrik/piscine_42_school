int	plus(int v1, int v2)
{	
	return (v1 + v2);
}

int	minus(int v1, int v2)
{	
	return (v1 - v2);
}

int	division(int v1, int v2)
{	
	return (v1 / v2);
}

int	mult(int v1, int v2)
{	
	return (v1 * v2);
}

int	modulo(int v1, int v2)
{	
	return (v1 % v2);
}
